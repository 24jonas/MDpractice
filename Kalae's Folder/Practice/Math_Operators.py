def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    if b == 0:
        return "Division by zero is not allowed."
    return a / b
def modulus(a, b):
    return a % b
def exponent(a, b):                 
    return a ** b
def floor_division(a, b):
    return a // b
def absolute(a):
    return abs(a)
def round_number(a):
    return round(a)
def power(a, b):
    return pow(a, b)
def max_value(a, b):
    return max(a, b)
def min_value(a, b):
    return min(a, b)
def sum_values(*args):
    return sum(args)           